-- *********** SELECT CƠ BẢN
SELECT *
FROM STUDENTS

-- *********** SELECT CÓ ĐIỀU KIỆN, AND, OR, NOT, IN, BETWEEN?
SELECT *
FROM STUDENTS
WHERE AGE BETWEEN 18 AND 21;

SELECT *
FROM STUDENTS
WHERE AGE >= 22 OR AGE <= 19;

SELECT *
FROM STUDENTS
WHERE AGE IN (18, 21);

SELECT *
FROM STUDENTS
WHERE AGE NOT IN (18, 21);

-- *********** DISTINCT
SELECT DISTINCT(CLASS)
FROM STUDENTS;

-- *********** UPDATE
UPDATE STUDENTS SET NAME = N'LO VĂN HIỀN' WHERE ID = 1;

SELECT *
FROM STUDENTS
WHERE ID = 1;

-- *********** DELETE 
DELETE STUDENTS WHERE ID = 8;

SELECT *
FROM STUDENTS;

-- *********** ORDER BY
-- Giảm dần
SELECT *
FROM STUDENTS
ORDER BY(AGE) DESC;

-- Tăng dần
SELECT *
FROM STUDENTS
ORDER BY(AGE) ASC;

-- *********** SELECT TOP
SELECT TOP 5 * FROM STUDENTS ORDER BY(AGE) DESC;

