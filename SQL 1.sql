CREATE DATABASE STUDENT_MANAGEMENT;
GO

USE STUDENT_MANAGEMENT;
GO

CREATE TABLE STUDENTS
(
    ID INT IDENTITY(1, 1) CONSTRAINT PK_STUDENT UNIQUE,
    AGE INT CHECK(AGE >= 18),
    NAME NVARCHAR(255),
    CLASS NVARCHAR(255)
);
GO

